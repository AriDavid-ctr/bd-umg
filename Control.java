    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        try (Connection con = conectar()) {
        String sql = "INSERT INTO persona (id, nombre, apellido, edad) VALUES (?, ?, ?, ?)";
        PreparedStatement ps = con.prepareStatement(sql);
        ps.setInt(1, Integer.parseInt(t1.getText()));
        ps.setString(2, t2.getText());
        ps.setString(3, t3.getText());
        ps.setInt(4, Integer.parseInt(t4.getText()));
        ps.executeUpdate();

        JOptionPane.showMessageDialog(this, "Registro insertado");

        modelo.addRow(new Object[]{
            t1.getText(), t2.getText(), t3.getText(), t4.getText()
        });
        t1.setText("");
        t2.setText("");
        t3.setText("");
        t4.setText("");

    } catch (Exception e) {
        JOptionPane.showMessageDialog(this, "****** Error: " + e.getMessage());
    }
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
      try (Connection con = conectar()) {
        String sql = "SELECT * FROM persona WHERE id=?";
        PreparedStatement ps = con.prepareStatement(sql);
        ps.setInt(1, Integer.parseInt(t1.getText()));
        ResultSet rs = ps.executeQuery();
        modelo.setRowCount(0);
        if (rs.next()) {
            modelo.addRow(new Object[]{
                rs.getInt("id"),
                rs.getString("nombre"),
                rs.getString("apellido"),
                rs.getInt("edad")
            });
            t2.setText(rs.getString("nombre"));
            t3.setText(rs.getString("apellido"));
            t4.setText(String.valueOf(rs.getInt("edad")));
        } else {
            JOptionPane.showMessageDialog(this, "no hay datos registrados");
        }

    } catch (Exception e) {
        JOptionPane.showMessageDialog(this, "** Error: " + e.getMessage());
    }
    }//GEN-LAST:event_jButton3ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        
        try (Connection con = conectar()) {
        String sql = "UPDATE persona SET nombre=?, apellido=?, edad=? WHERE id=?";
        PreparedStatement ps = con.prepareStatement(sql);
        ps.setString(1, t2.getText()); 
        ps.setString(2, t3.getText()); 
        ps.setInt(3, Integer.parseInt(t4.getText())); 
        ps.setInt(4, Integer.parseInt(t1.getText())); 

        int filas = ps.executeUpdate();
        if (filas > 0) {
            JOptionPane.showMessageDialog(this, " Registro actualizado correctamente");

            modelo.setRowCount(0);
            String sqlSelect = "SELECT * FROM persona";
            Statement st = con.createStatement();
            ResultSet rs = st.executeQuery(sqlSelect);

            while (rs.next()) {
                modelo.addRow(new Object[]{
                    rs.getInt("id"),
                    rs.getString("nombre"),
                    rs.getString("apellido"),
                    rs.getInt("edad")
                });
            }
        } else {
            JOptionPane.showMessageDialog(this, "Ô∏è No se encontro el ID para actualizar");
        }

    } catch (Exception e) {
        JOptionPane.showMessageDialog(this, "***Error al actualizar: " + e.getMessage());
    }
    }//GEN-LAST:event_jButton2ActionPerformed

